package com.assignment1.problem5;

public class Calculate {
	static int add(int a, int b)
	{
		int sum = a+b;
		return sum;
	}
	
	static int subtract(int a, int b)
	{
		int diff = a-b;
		return diff;
	}
	
	static double multiply(double a, double b)
	{
		double product = a*b;
		return product;
	}
	
	static double divide(double a, double b)
	{
		double quot = a/b;
		return quot;
	}
}
