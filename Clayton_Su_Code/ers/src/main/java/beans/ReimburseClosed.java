package beans;

public class ReimburseClosed
{
	int ridC;
	int amount;
	String status;
	int eid;
	int mid;
	public int getRidC()
	{
		return ridC;
	}
	public void setRidC(int ridC)
	{
		this.ridC = ridC;
	}
	public int getAmount()
	{
		return amount;
	}
	public void setAmount(int amount)
	{
		this.amount = amount;
	}
	public String getStatus()
	{
		return status;
	}
	public void setStatus(String status)
	{
		this.status = status;
	}
	public int getEid()
	{
		return eid;
	}
	public void setEid(int eid)
	{
		this.eid = eid;
	}
	public int getMid()
	{
		return mid;
	}
	public void setMid(int mid)
	{
		this.mid = mid;
	}
	
}
