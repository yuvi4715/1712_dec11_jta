package fileio;

public class Employee {	//#27
	int employeeID;
	String firstName;
	String lastName;
	String role;
	
	public Employee(int employeeID, String firstName, String lastName, String role) {
		super();
		this.employeeID = employeeID;
		this.firstName = firstName;
		this.lastName = lastName;
		this.role = role;
	}
}
