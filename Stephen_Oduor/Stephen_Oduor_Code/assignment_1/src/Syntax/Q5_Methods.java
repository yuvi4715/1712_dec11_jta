public class Q5_Methods {

    public Q5_Methods(){

    }

    //Adds two numbers
    public int add(int a, int b){
        return a + b;
    }

    //Subtracts two numbers
    public int subtract(int a, int b){
        return a - b;
    }

    //Divides two numbers
    public int divide(int a, int b){
        return a / b;
    }


}
