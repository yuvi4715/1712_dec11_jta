package com.revature.week1.Syntax;

public class CalculatorMethods {

	//add method
	int add(int one, int two) {
		
		int total = 0;
		total = one + two;
		return total;
	}
	
	//subtract method
	int subtract(int one, int two) {
		
		int total = 0;
		total = one - two;
		return total;
	}

	//multiply method
	int multiply(int one, int two) {
	
		int total = 0;
		total = one * two;
		return total;
	}

	//divide method
	int divide(int one, int two) {
	
		int total = 0;
		total = one / two;
		return total;
	}
}
