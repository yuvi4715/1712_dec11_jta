
package assign_1;

public class Number1 {
	
	public static void main(String[] args) {
		int x = 6; //creates an integer x and gives it a value of 6
		int y = 3; //creates an integer y and gives it a value of 3
		System.out.println("The value of x + y = " + (x + y)); //gets the sum of x and y and prints it
		System.out.println("The value of x - y = " + (x - y)); //gets the difference between x and y and prints it
		System.out.println("The value of x * y = " + (x * y)); //gets the product of x and y and prints it
		System.out.println("The value of x / y = " + (x / y)); //gets the quotient of x and y and prints it

	}
	
}
