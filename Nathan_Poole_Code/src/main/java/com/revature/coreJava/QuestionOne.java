//Question one
//Write a program to add, subtract, multiply 
//and divide two non-zero hard-coded numbers.

public class QuestionOne {

	public static void main(String[] args) {
		int a = 1, b = 3;
		System.out.println(a + " + " + b + " = " + (a + b));	//add
		System.out.println(a + " - " + b + " = " + (a - b));	//subtract
		System.out.println(a + " * " + b + " = " + (a * b));	//multiply
		System.out.println(a + " / " + b + " = " + ((double)a / b));	//divide

	}

}
