package com.revature.day1;

public class PrintNumberinWord {
	
	int number = 6;
	
	{
		switch (number)
		{
			case 1: number = 1;
			{
			System.out.println("ONE");
			break;
			}
			case 2: number = 2;
			{
			System.out.println("TWO");
			break;
			}
			case 3: number = 3;
			{
			System.out.println("THREE");
			break;
			}
			case 4: number = 4;
			{
			System.out.println("FOUR");
			break;
			}
			case 5: number = 5;
			{
			System.out.println("FIVE");
			break;
			}
			case 6: number = 6;
			{
			System.out.println("SIX");
			break;
			}
			case 7: number = 7;
			{
			System.out.println("SEVEN");
			break;
			}
			case 8: number = 8;
			{
			System.out.println("EIGHT");
			break;
			}
			case 9: number = 9;
			{
			System.out.println("NINE");
			break;
			}
			default: 
			{
				if (number > 9)
				{
			System.out.println("you have a strange number.");
			break;
			}
		}
		
	}
	

	}
}
