package com.revature.question5;

//5.	In one project, create two classes. One class should contain only methods (add, subtract, multiply and divide). 
// The other class should contain only the main() method which calls each of the methods from the previous class.
public class Question5A {
	
	// This method adds the two parameters
	public int sum(int first, int second) {
		return first + second;
	}
	
	// This method subtracts the two parameters
	public int difference(int first, int second) {
		return first - second;
	}
	
	// This method multiplies the two parameters
	public int product(int first, int second) {
		return first * second;
	}
	
	// This method divides the two parameters
	public int quotient(int first, int second) {
		return first / second;
	}
}
